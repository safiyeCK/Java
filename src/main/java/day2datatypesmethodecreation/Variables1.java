package day2datatypesmethodecreation;

public class Variables1 {
    public static void main(String[] args) {



        // ornek 1: Sehirismi icin bir variable olusturup once Ankara sonra da
        // Izmir degerlerini atayip ekrana yazdirin

        String cityName= "Ankara";

        System.out.println(cityName);

        cityName= "Izmir";

        System.out.println(cityName);


        // Sehir plaka kodu icin bir variable olusturun
        //once 34 degeri olusturun sonra 34 ve yazdir

        byte cityKode = 34;

        System.out.println(cityKode);

        cityKode =35;

        System.out.println(cityKode);

        // ornek 3: Basarili mi soruusu icin bir variable olusturun
        //once false sonra basarli olup true yazdirin

        boolean basariliMi = false;

        System.out.println(basariliMi);

        basariliMi= true;

        System.out.println(basariliMi);
    }
}
